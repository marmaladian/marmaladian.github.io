: title: Oatmeal biscuits

* 1.5 cups rolled oats</li>
* 1 cup plain flour</li>
* 1 cup dried coconut</li>
* 1 tsp bicarb soda</li>
* 0.5 tsp salt</li>
* 1 tsp cinnamon</li>
* 1 cup brown sugar</li>
* 0.5 cup butter</li>
* 3 tbsp golden syrup</li>
* 1 tsp vanilla extract</li>
* 1 tbsp soy milk</li>

  Preheat oven to 180°C.

  Mix oats, flour, coconut, bicarb, salt and cinnamon together. Cream butter and sugar then add golden syrup and vanilla. Combine with dry ingredients. Mix in soy milk, shape into 16 large balls.

  Bake for 15 minutes or until edges crisp.