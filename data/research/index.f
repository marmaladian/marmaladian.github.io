2 MoveMemorE vehicle motion replay 
  For my Bachelor's capstone project, my team developed VR software that uses GPS and inertial data to replay recorded journeys. The software connects directly to the {{NOVA motion platform|https://www.eight360.com/}} to control its 360° motion, synchronising physical movement with the VR visuals to recreate the sensation of the original journey.

